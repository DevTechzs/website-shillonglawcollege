  function getData() {
    var json = new Object();
    json.URLCategoryID = '24';
    let obj = {};
    obj.Module = "Admin";
    obj.Page_key = "getAllNews";
    obj.JSON = json;
    TransportCall(obj);
  }

  function onSuccess(rc) 
  {
    //console.log(rc);

    if (rc.return_code) {
      switch (rc.Page_key) {
        case "getAllNews":
       debugger;
          var news = rc.return_data.news;
          var news_details = rc.return_data.news_details;
          if(news.length>0)
            loaddata(news, news_details);
          break;
          default:
            alert(rc.Page_key);
      }
    } 
    else {
      alert(rc.return_data);
    }
  }

  function loaddata(news, news_details)
  {
    var div_news='';
    var div_news2='';
    news.forEach(function (n_data) {
      div_news2 += '&nbsp;&nbsp;&nbsp;&nbsp;'+n_data['Title']+'&nbsp;&nbsp;&nbsp;&nbsp;||';
      if(n_data['IsNew']=='1')
        div_news += '<span style="font-size:x-small; font-weight:bold; color:red; text-shadow: 2px 2px 4px yellow;">NEW</span>';
      div_news += '<p style="margin-bottom:6px !important;">'+n_data['Title']+'</p>';

      var count_nd=0;
      news_details.forEach(function (nd_data) {
        if(nd_data['NewsID']==n_data['NewsID'])
          count_nd++;
      });
      if(count_nd>0){
        div_news += '<ol>';
        news_details.forEach(function (nd_data) 
        {
          if(nd_data['NewsID']==n_data['NewsID'])
          {
            if(nd_data['TypeID']=="1")
              div_news += '<li>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<a href="'+nd_data['FileLink']+'" target="_blank" title="VIEW DOCUMENT" style="font-size:small !important; font-weight:bold !important;"><i class="fas fa-paperclip"></i>&nbsp;&nbsp;'+nd_data['Title']+'</a></li>';
            else
              div_news += '<li>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<a href="'+nd_data['FileLink']+'" target="_blank" title="OPEN LINK" style="font-size:small !important; font-weight:bold !important;"><i class="fas fa-external-link-alt"></i></i>&nbsp;&nbsp;'+nd_data['Title']+'</a></li>';
          }
        });
        div_news += '</ol>';
      }
      div_news += '<hr>';
    });

    $('#div_news').empty();
    $('#div_news1').empty();
    $('#div_news2').empty();
    $('#div_news').append(div_news.slice(0, -4));
    $('#div_news1').append(div_news.slice(0, -4));
    $('#div_news2').append(div_news2.slice(0, -2));
    localStorage.setItem("div_news",div_news.slice(0, -4));
    localStorage.setItem("divnews2",div_news2);
  }
</script>