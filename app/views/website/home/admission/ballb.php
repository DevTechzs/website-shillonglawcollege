                            <h1>Introduction</h1>
                            <p>The Five Year BA.LL.B Integrated Degree Programme comprises of fifty one (51) Course, spread over Ten (10) Semesters, Each Semester of the Programme consists of Five Courses except the Semester Ten (10) which has Six (6) Courses. This programme contains Eighteen (18) Compulsory Courses, Four (4) Clinical Courses, Six (6) Optional Courses, Eight (8) Honours Courses and Fifteen (15) Liberal Courses.</p>
                            <p>Each Course except Two (2) Clinical Courses i.e., Drafting, Pleading & Conveyance and Moot Court Exercise and Internship, have been divided into four units and the pattern of evaluation involves setting of two questions from each unit in the End Semester Examination.</p>
                            <p>As provided in the Regulation relating to the Five Year Integrated B.A. LLB (Hons) Degree Programme, the Compulsory Law Courses, Optional Courses, Honours Courses, Liberal Courses, and Two (2) Clinical Courses i.e., Professional Ethics and Professional Accounting System and Alternative Dispute Resolution will be evaluated in the ratio of 40:60 i.e., 40% weightage is allotted for continuous sessional works and 60% weightage for the end Semester Examination. The other two Clinical Courses will be evaluated as follows:</p>

                            <ol class="members">
                                <li><strong>Drafting, Pleading and Conveyance &mdash;</strong> The Course will be taught through class instruction and simulation exercises preferably with assistance of practicing Lawyers/Retired Judges. Apart from teaching the relevant provisions of Law, the course may include not less than 15 practical exercises in drafting carrying a total of 45 marks (3 mark for each) and 15 exercises in conveyancing carrying another 45 marks (3 marks for each exercise) remaining 10 marks will be given for viva voce.</li>
                                <li><strong>Moot Court Exercise and Internship &mdash;</strong> This paper may have 3 components 30 marks and a viva voce for 10 marks:
                                    &nbsp;&nbsp;<br />i.	<span style="font-style:italic; font-weight:bold; font-size:small;">Moot Court (30 marks) -</span> Every student may be required to do at least 3 moot Courts in a year with 10 marks for each. The Moot Court work will be assigned problem and it will be evaluated for 5 marks for written submission and 5 marks for oral advocacy.
                                    &nbsp;&nbsp;<br />ii.	<span style="font-style:italic; font-weight:bold; font-size:small;">Observation of Trial in Two Cases, one Civil and one Criminal (30 Marks) -</span> Students may be required to attend two trials in the course of the last two or three years of B.A. LL.B studies. They will maintain a record and enter the various steps observed during their attendance of different days in the court assignment. This scheme will carry 30 marks.
                                    &nbsp;&nbsp;<br />iii.	<span style="font-style:italic; font-weight:bold; font-size:small;">Interviewing Techniques and Pre-trial preparations and Internship Dairy (30 marks) -</span> Each student will observe two interviewing sessions of clients at the Lawyer’s Office/Legal Aid Office and record the proceedings in a diary, which will carry 15 marks. Each student will further observe the preparation of documents and Court papers by the advocate and the procedure for the filling of the suit/petition. This will be recorded in the diary, 3which will carry 15 marks.
                                    &nbsp;&nbsp;<br />iv.	<span style="font-style:italic; font-weight:bold; font-size:small;">Viva Voce Examination (10 marks) -</span> The fourth component of this paper will be Viva Voce Examination of all the above three aspects. This will carry 10 marks.
