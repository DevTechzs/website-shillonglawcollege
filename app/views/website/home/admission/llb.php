                                            &nbsp;&nbsp;<br />ii.	<span style="font-style:italic; font-weight:bold; font-size:small;">Observation of Trial in two Cases, One Civil and one Criminal (30 marks) -</span> Students may be required to attend two trials in the course of the last three years of LL.B studies. They will maintain a record and enter the various steps observed by them during their attendance on different days in the court. This scheme will carry 30 marks.
                                            &nbsp;&nbsp;<br />iii.	<span style="font-style:italic; font-weight:bold; font-size:small;">Interview Techniques and Pre-trial preparations (30 marks) -</span> Each student will observe two interviewing sessions of clients at the lawyer's office/Legal Aid Office and record the proceeding in a diary, which will carry 15 marks. Each student will further observe the preparation of documents and Court papers and the procedure for the filing of the suit/petition. This will be recorded in the diary, which will carry 15 marks.
                                            &nbsp;&nbsp;<br />iv.	<span style="font-style:italic; font-weight:bold; font-size:small;">Viva Voce Examination (10 marks) -</span> The fourth component of this paper will be the Viva Voce Examination of all the above three aspects. This will carry 10 marks.
