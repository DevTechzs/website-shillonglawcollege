                    <b>COURSE OF STUDY FOR THE LL.B (PASS) DEGREE COURSE</b>
                    <p>The Three-year LL.B (Pass) Degree Course comprises of 30 papers, spread over six semesters. Each semester consists of five papers. The whole course contains twenty compulsory papers, four clinical papers and six optional papers. </p>
                    <p>Each course except the two clinical courses i.e., (i) Drafting, Pleading &amp; Conveyancing, and (ii) Moot Court Exercise and Internship, has been divided into four units with 30 to 35 class lectures per course and the pattern of evaluation will be setting of two questions from each unit in the End Semester Examination. Servicing of the two clinical courses is explained in page 4 of the Prospectus.</p>
                    <p>The Compulsory Law papers, optional papers and two clinical paper viz., (i) Professional Ethics &amp; Professional Accounting System and (ii) Alternate Dispute Resolution in Semester III &amp; IV respectively, shall be of 100 marks each containing eight questions and every examination shall be of three hour duration at the end of each Semester.</p>

